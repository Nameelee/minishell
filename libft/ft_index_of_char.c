#include "libft.h"
#include <assert.h>

int	ft_index_of_char(char *str, char c)
{
	int	i;

	i = 0;
	if(!str)
		return(-1);
	while (str[i])
	{
		if (str[i] == c)
			return (i);
		i++;
	}
	return (-1);
}